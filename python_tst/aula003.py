'''
Tipos de tipagem = Dinamica / Forte
str -> string -> texto, sempre colocar dentro de aspas
'''
print(1234567890)

# Aspas Simples
print('String com aspas simples')

# Aspas Duplas
print('String com aspas dulpas')

# Escape
print('String com aspas simples incluindo \" aspas no meio do texto')

# r
print(r'String com aspas simples incluindo \" aspas no meio do texto')

# Forma mais simples para usar aspas dentro do texto, deve sempre inverter as aspas ' "" ' ou " '' "
print('String com aspas simples incluindo "aspas" no meio do texto')
